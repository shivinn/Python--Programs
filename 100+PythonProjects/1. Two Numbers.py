# Program to add two numbers
# Solution 1 (with pre-trained Variables)

num1 = 30
num2 = 40

sum = num1 + num2

print(" The sum of given two numbers is: ", sum)

# Solution 2 (with user-defined variables)

num1 = float(input(f" Enter Number 1: "))
num2 = float(input(f" Enter Number 2: "))

sum = num1 + num2

print(" The sum of given two numbers is: ", sum)
